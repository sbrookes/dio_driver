/*

  Header file used to typedef the standard
  types used by the legacy SuperDARN ROS code
  in terms of standard linux types.

  Author: Scott Brookes
  Date: 2.2.13

 */

#ifndef DEF_GUARD_ROS_TYPES_H
#define DEF_GUARD_ROS_TYPES_H

#include <sys/types.h>

typedef  u_int8_t  uint8 ;
typedef    int8_t   int8 ; 
typedef u_int16_t  uint16;
typedef   int16_t   int16;
typedef u_int32_t  uint32;
typedef   int32_t   int32;
typedef u_int64_t  uint64;
typedef   int64_t   int64;

#endif
